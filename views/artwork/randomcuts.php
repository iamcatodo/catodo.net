    <h2>Random cuts</h2>
    <p style="margin-top:50px">
      <strong>Random cuts</strong> is a live performance of generative art. The artwork is inspired to the act of cutting, as constructive or destructive fact. Using a custom software that generates a “structure” cutting the randomness with video and audio signals. The software is managed in real time by a <a href="https://www.leapmotion.com/">Leap Motion</a> interface using an external signal processor unit to modulate the sound waves. From white noise signals to complex additive synth sounds and from blank video to complex structures of lines, the performance lives in a bordeline state, between order and chaos.
    </p>
    <p>
      This performance has been presented during the <a href="http://www.generativeart.com/">XVI Generative Art International Conference</a> 2013, <a href="http://www.newfolder.it/">New Folder</a> festival 2013, <a href="http://www.mutaforma.net/">MutaForma</a> art exhibition 2014, <a href="http://www.theothersfair.com/">The Others</a> 2014 art exhibition.
      Moreover, a generative installation of Random cuts has been presented during the <a href="http://www.prolocoscarperia.it/-sul-filo-della-lama.html">Sul filo della lama</a> art exhibition in Scarperia, Florence (Italy).
    </p>
