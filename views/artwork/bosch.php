    <h2>The Bosch's garden</h2>
    <p style="margin-top:50px">
      <strong>The Bosch's garden</strong> is a generative artwork based on the famous painting <a href="https://en.wikipedia.org/wiki/The_Garden_of_Earthly_Delights">The Garden of Earthly Delights</a> of Hieronymus Bosch. The artwork is generated using the pointillism technique, picking random pixels and colors from the original painting.
      For each pixel the algorithm draws a triangle with a random width. The idea to use triangles comes from the esoteric symbolism of the Bosch painting.
    </p>
    <p>
      This artwork has been presented during the <a href="http://www.doloresart.it/galleria/2014/04/19/garden-of-eden-group-exhibition/">Garden of Eden</a> art exhibition at Palazzo Viti of Volterra (Italy) in 2014.
