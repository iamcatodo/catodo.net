    <h2>Noise Curve</h2>
    <p style="margin-top:50px">
      <strong>Noise Curve</strong> is a sound (and visual) installation that searches for hidden information in almost silent sound samples. The audio samples come from field recording in uninhabited places.
    </p>
    <p>
        Using a custom software, the sound samples are analyzed with a <a href="https://en.wikipedia.org/wiki/Fast_Fourier_transform">Fast Fourier Transform</a> (FFT) algorithm, that produces the frequency domain of the signal.
        These frequencies are used to plot a curve on the screen that leaves an indelible mark over time.  This curve is used to represent the sound in a single two-dimensional image.
    </p>
    <blockquote>
      Noise curves are a common way to measure and specify background noise in unoccupied buildings and spaces.
      Their purpose is to produce a single value representation of a complete sound spectrum.
      <i>From <a href="https://en.wikipedia.org/wiki/Noise_curve">Wikipedia</a></i>
    </blockquote>
    <p>
      <i>Materials:</i> Mini projector LCD, Rasperry Pi, custom software, headphones, sample sounds, Kodak Carousel S-AV 2010.
