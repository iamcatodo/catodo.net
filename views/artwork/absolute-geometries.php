    <h2>Absolute geometries</h2>
    <p style="margin-top:50px">
      <strong>Absolute geometries</strong> is an interactive installation designed with <a href="http://www.massimodesiato.com">Massimo Desiato</a> (a.k.a. teschiourbano) for the <a href="http://www.newfolder.it">New Folder</a> 2014 festival.
    </p>
    <p>
      This is an ongoing project that collects some of the techniques developed by working on research and the interaction between different materials. The geometric structures are in connection with each other and with a solid base, create a network in an attempt to dominate the passions which inevitably transform those simple ties in a real symbiosis. The result is a collection that try to imitate the complexity of human relationships.
    </p>
    <p>
      We created a geometric structure using ribbons of red cotton and we applied vibration sensors on it. If a visitor touch one of these ribbons, the vibration sensor activates a random audio sample. The audio samples have been recorded by the actresses <i>Valentina Papagna</i>, <i>Laura Forlani</i> and <i>Lara Romano</i>. The result is a generative novel that follows a specific context in a random fashion way.
    </p>
    <p>
      <i>Technical note:</i> the vibration sensors are managed using a <a href="https://www.raspberrypi.org/">Raspeberry Pi</a> using the GPIO interface with a Python custom software. We used the <a href="https://www.adafruit.com/product/1766">Fast Vibration Sensor Switch SW-18010P</a> sold by <a href="https://www.adafruit.com/">Adafruit</a>.
    </p>
    <p>
      <i>Materials:</i> ribbons of red cotton, Rasperry Pi, vibration sensors, custom software, stereo speakers
