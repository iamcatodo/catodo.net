    <h2>Condizione Umana</h2>
    <p style="margin-top:50px">
      <strong>Condizione Umana</strong> is an interactive installation designed with <a href="http://www.valentinapeter.com/">Valentina Peter</a> (a.k.a. <i>Effemeride</i>), an Italian artist that works with books and papers. The artwork wants to be a reflection on the human condition (<i>condizione umana</i> in Italian). We used an Italian book of 1920 and a pair of windows from a house in <a href="https://en.wikipedia.org/wiki/Turin">Turin</a> from the same era.
    </p>
    <p>
        We selected 8 pages from the book and extracted 8 sentences that represent a human condition. Each sentence has been converted in speech, using a female and a male voices.
        Each sound sample has been divided in sub-samples and stored in a <a href="https://www.raspberrypi.org/">Rasperry Pi</a> computer. Usign a custom software, the computer select a page from the book and read a random phrase from the collection of samples.
        The result is a endless dialogue between woman and man. When the computer selects a page it also turns on a LED, located under the page to give a visual feedback to the visitors.
        The interaction part is made using a <a href="https://www.adafruit.com/product/189">PIR motion sensor</a> that turns on the computer algorithm when a visitor approaches the installation.
    </p>
    <p>
        The source code of the custom software is available <a href="https://github.com/iamcatodo/CondizioneUmana">here</a>.
    </p>
    <p>
      <i>Materials:</i> Paper, wood, Rasperry Pi computer, custom software, LED, PIR motion sensor, sound speakers.
    </p>
