      Genera is an audio/video performance composed by a video projection, designed by Catodo, and a soundtrack, composed by Globster.
      The performance is based on the idea of <i>generate/degenerate</i> existing objects like photos, videos, or sounds.
      The video is produced by a custom software that mixes original photos taken during the artist residency. The photos are manipulated in real time using a reflection + glitch algorithm driven by a MIDI controller (<a href="http://www.akaipro.com/product/lpd8">AKAI LPD8</a>).
      The soundtrack is composed by different audio samples taken in Villa Pravernara and edited in post-production. The soundtrack is then manipulated in real time using a sampler with digital effects.
