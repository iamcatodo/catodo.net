    <h2>Perimetri</h2>
    <p style="margin-top:50px">
      <strong>Perimetri</strong> is a video mapping installation presented at <a href="http://www.mutaforma.net/">MutaForma</a> 2014, the Abruzzo art biennal, at the <a href="http://www.fondazionemichetti.it">MuMi museum</a> of Francavilla Al Mare (Italy). The Perimetri project has been realized together with <a href="http://lorenzokamerlengo.tumblr.com/">Lorenzo Kamerlengo</a>, a young artist from Pescara. The project is based on the paper “The Picture Frame: An Aesthetic Study” of Georg Simmel and it uses some pictures taken from the artworks of <a href="https://en.wikipedia.org/wiki/Francesco_Paolo_Michetti">Francesco Paolo Michetti</a> (1851-1929), the artist to whom is dedicated the MuMi museum.
    </p>
    <p>
      The custom software produces random cuts of some famous Michetti’s pictures and project it on parallelepipeds in a space, using a self-made video mapping software running on a Rasperry Pi. We used two projectors to map the solids in all the sides, in order to create an immersive environment where visitors can pass in. The source code of the project, including the video mapping, has been released as open source <a href="https://github.com/iamcatodo/perimetri">here</a>.
    </p>
    <p>
      <i>Materials:</i> Wood, 2 Projectors Full HD, 2 Raspberry Pi, custom software
